console.log(!true); // false
console.log(!false); // true
console.log(!!true); // true
console.log(!!false); // false
// !!값 을 한 경우 값에 대한 bool값을 반환 해준다.

console.log(!!1); // true
console.log(!!0); // false
console.log(!!'a'); // true
console.log(!!''); // false
console.log(!!undefined); // false
console.log(!!{}); // true
console.log(!!null); // false
console.log(!!Infinity); // true